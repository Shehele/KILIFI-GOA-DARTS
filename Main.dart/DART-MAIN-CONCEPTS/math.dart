//import dart math with library for mathematical operations
import 'dart:math';

void main() {
  int num1 = 140;
  double num2 = 30.89;

  //add the two
  print(num1 + num2);
  double result = num1 + num2;
  double result1 = num1 - num2;
  double result2 = num1 * num2;
  double result3 = num1 / num2;

  print(result);
  print(result1);
  print(result2);
  print(result3);

  //print maximum or minimum number from an option
  print(min(50, 100));
  print(max(50, 100));

  //square root of a number
  print(sqrt(81));

  //
}
