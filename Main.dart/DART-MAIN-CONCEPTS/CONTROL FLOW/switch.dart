//switch is the short form of if else
//it uses case and breaks
void main() {
  int age = 65;
  switch (age) {
    case 1:
      print("the value is 1");
      break;
    case 2:
      print("the value is 2");

      break;
    case 3:
      print("the value id 3");

    default:
      print("the age is not in the range given");
  }
}
